def switch_zero_one(number):
    """
    switch_zero_one
    parameter:
        number
            The input number to be switched, should be either a 0 or 1
    return:
        If the input number is 1, a 0 is returned and vice versa

    Essentially alternates a number between 1 and 0. If a 1 is given, a 0 is returned and
    vice versa.
    """
    return 1 - number
